import Foundation

enum Fonts {
    enum Inter: String {
        /// Regular font weight which corresponds to the weight value of 400
        case regular = "Inter-Regular"
        
        /// Medium font weight which corresponds to the weight value of 500
        case medium = "Inter-Medium"
        
        /// SemiBold font weight which corresponds to the weight value of 600
        case semiBold = "Inter-SemiBold"
        
        /// Bold font weight which corresponds to the weight value of 700
        case bold = "Inter-Bold"
    }
}
